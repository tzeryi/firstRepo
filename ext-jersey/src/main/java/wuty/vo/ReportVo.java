package wuty.vo;

public class ReportVo {

	private String waferId = null;
	private String ex = null;
	private String ey = null;
	private String tx = null;
	private String ty = null;

	public String getWaferId() {
		return waferId;
	}
	public void setWaferId(String waferId) {
		this.waferId = waferId;
	}
	public String getEx() {
		return ex;
	}
	public void setEx(String ex) {
		this.ex = ex;
	}
	public String getEy() {
		return ey;
	}
	public void setEy(String ey) {
		this.ey = ey;
	}
	public String getTx() {
		return tx;
	}
	public void setTx(String tx) {
		this.tx = tx;
	}
	public String getTy() {
		return ty;
	}
	public void setTy(String ty) {
		this.ty = ty;
	}
}
